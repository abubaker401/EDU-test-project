const contractAddress = '0xD70a936E8206f20e5ace6A86e27ea602B635bFF7'; // Replace with your deployed contract address

let provider;
let signer;
let contract;

async function initialize() {
  if (typeof window.ethereum === 'undefined') {
    alert('Please install MetaMask to interact with this app.');
    return;
  }

  // Initialize Ethereum provider
  provider = new ethers.providers.Web3Provider(window.ethereum);
  signer = provider.getSigner();
  contract = new ethers.Contract(contractAddress, contractABI, signer);

  await window.ethereum.request({ method: 'eth_requestAccounts' });
}

document.getElementById('mint-form').addEventListener('submit', async (event) => {
  event.preventDefault();
  const uri = document.getElementById('mint-uri').value;

  try {
    const tx = await contract.mint(await signer.getAddress(), uri);
    await tx.wait();
    alert('NFT Minted!');
  } catch (err) {
    console.error('Error minting NFT', err);
  }
});

document.getElementById('auction-form').addEventListener('submit', async (event) => {
  event.preventDefault();
  const tokenId = document.getElementById('auction-token-id').value;
  const startingPrice = document.getElementById('auction-price').value;
  const auctionDuration = document.getElementById('auction-duration').value;

  try {
    const tx = await contract.createAuction(tokenId, startingPrice, auctionDuration);
    await tx.wait();
    alert('Auction Created!');
  } catch (err) {
    console.error('Error creating auction', err);
  }
});

document.getElementById('view-nft-details').addEventListener('click', async () => {
  const tokenId = document.getElementById('view-token-id').value;

  try {
    const [owner, uri] = await contract.getNFTDetails(tokenId);
    document.getElementById('nft-owner').innerText = owner;
    document.getElementById('nft-uri').innerText = uri;
  } catch (err) {
    console.error('Error fetching NFT details', err);
  }
});

// Initialize connection on page load
window.onload = initialize;
